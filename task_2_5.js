"use strict";

// 5. Реализовать четыре основные арифметические операции в виде функций с двумя
// параметрами. Обязательно использовать оператор return.

function plus(a, b) {
    return a + b;
}

function minus(a, b) {
    return a - b;
}

function mult(a, b) {
    return a * b;
}

function div(a, b) {
    return a / b;
}

alert(plus(5, 7));
alert(minus(7, 5));
alert(mult(3, 5));
alert(div(8, 3));
